

public class ifelseif {
    public static void main(String args[])
    {
        int no=-10;

        if(no>0)
        {
            System.out.println("given no. is positive.");
        }
        else if (no<0)
        {
            System.out.println("given no. is negative.");
        }
        else
        {
            System.out.println("given no. is zero");
        }
    }
}
