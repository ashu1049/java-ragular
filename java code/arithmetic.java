//Write a Java Program for finding sum/sub/mul/div 2 numbers?

public class arithmetic {
    public static void main(String args[])
    {
        int no1=20;
        int no2=10;

        System.out.println("sum of two numbers is "+ no1 + "+" + no2 + "="+ (no1+no2));
        System.out.println("sub of two numbers is "+ no1 + "-" + no2 + "="+ (no1-no2));
        System.out.println("mul of two numbers is "+ no1 + "*" + no2 + "="+ (no1*no2));
        System.out.println("div of two numbers is "+ no1 + "/" + no2 + "="+ (no1/no2));
    }
}
