class demoprogram {
    public static void main(String args[]) {
        byte a = 5;
        short b = 10;
        int c = 15;
        long d = 20;
        boolean e = true;
        float f = 1.5f;
        double g = 10.0;
        char h = 'h';

        System.out.println("value of byte is:" + a);
        System.out.println("value of short is:" + b);
        System.out.println("value of int is:" + c);
        System.out.println("value of long is:" + d);
        System.out.println("value of boolean is:" + e);
        System.out.println("value of float is:" + f);
        System.out.println("value of double is:" + g);
        System.out.println("value of character is:" + h);
    }
}
