public class bitdemo {
 public static void main(String args[])
 {
    int no1=10;
    int no2=9;

    System.out.println("bitwise no1&no2 :"+ (no1&no2));
    System.out.println("bitwise no1|no2 :"+ (no1|no2));
    System.out.println("bitwise no1^no2 :"+ (no1^no2));
    System.out.println("bitwise complement no1 :"+ ~(no1));
 }
}
